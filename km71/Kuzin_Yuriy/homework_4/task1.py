"""
Умова: Дано два цілих числа. Вивести найменше з них.

Вхідні дані: користувач вводить ціле число

Вихідні дані: вивести ціле число
"""
x = int(input("ВВедіть перше чило "))
y = int(input("Введіть друге число "))
answer = ""
if x<y:
    answer = y
elif x>y:
    answer = x
else:
    answer = "Числа рівні" 
print(answer)
